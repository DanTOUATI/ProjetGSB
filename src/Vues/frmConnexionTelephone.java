    private void btnContinuerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnContinuerMouseClicked
        // TODO add your handling code here:
        
        ConnexionBdd cnx = new ConnexionBdd();
        FonctionsMetier fm= new FonctionsMetier();     
        
        StringBuilder sb = new StringBuilder();
        boolean erreur = false;
        for(char c : txtNum.getText().toCharArray())
        {
            if(!Character.isDigit(c))
            {
                sb.append(c);
                erreur = true; 
            } 
        }
        if((txtMail.getText().isEmpty()) || (txtNom.getText().isEmpty())|| (txtNum.getText().isEmpty())|| (txtPrenom.getText().isEmpty()) || erreur)
        {
            if(erreur)
            {
                 JOptionPane.showMessageDialog(this, "Le numero ne peut pas contenir de lettres");
            }
            else
            {
                 JOptionPane.showMessageDialog(this, "Certains champs sont vides ! Merci de bien vouloir tous les remplir!");
            }
            this.setVisible(true);
           
        }
        else 
        {
            if (txtMail.getText().matches(".+@.+\\.[a-z]+"))
            {
                fm.InsererNum(txtPrenom.getText(),txtNom.getText(),txtNum.getText(),txtMail.getText());
                this.setVisible(false);
                frmTableauDeBord frmTDB = new frmTableauDeBord();
                frmTDB.setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(this, "l'adresse mail n'est pas valide");
            }
            
        }   
    }//GEN-LAST:event_btnContinuerMouseClicked

